var num1 = 10;
var num2 = 20;

// arithmetic operation
console.log("arithmetic operation");
console.log(num1 + num2);
console.log(num1 - num2);
console.log(num1 * num2);
console.log(num2 / num1);
console.log(num1 % num2);

// increment and decrement
console.log("increment and decrement:");
num1++;
console.log(num1);
num2--;
console.log(num2);

// shorthand method
console.log("shorthand method:");
console.log((num1 += 10));
console.log((num1 -= 10));
console.log((num1 *= 10));
console.log((num1 /= 10));
console.log((num1 %= 10));
