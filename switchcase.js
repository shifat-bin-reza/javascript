var value = 3;
switch (value) {
  case 0:
    console.log("This is zero");
    break;
  case 1:
    console.log("This is one");
    break;
  case 2:
    console.log("This is two");
    break;
  case 3:
    console.log("This is three");
    break;
  case 4:
    console.log("This is four");
    break;
  case 5:
    console.log("This is five");
    break;
  default:
    console.log("This is String");
    break;
}
